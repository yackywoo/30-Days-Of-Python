print(2 + 3)   # addition(+)
print(3 - 1)   # subtraction(-)
print(2 * 3)   # multiplication(*)
print(3 / 2)   # division(/)
print(3 ** 2)  # exponential(**)
print(3 % 2)   # modulus(%)
print(3 // 2)  # Floor division operator(//)

# Checking data types

print(type(10))                  # Int
print(type(3.14))                # Float
print(type(1 + 3j))              # Complex
print(type('Asabeneh'))          # String
print(type([1, 2, 3]))           # List - mutable, specific order, can contain diff types of data - USES INDEX
print(type({'name':'Asabeneh'})) # Dictionary - ordered key-value pairs accessed by keys, can change the key / value pairs, can hold diff types of data as well
print(type({9.8, 3.14, 2.7}))    # Set - mutable, unordered, no duplicates allowed, can contain diff types of data - USES INDEX
print(type((9.8, 3.14, 2.7)))    # Tuple - immutable, specific order, once created cannot add change or remove items - USES INDEX
